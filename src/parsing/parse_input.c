#include "../../minishell.h"

void	parse_input(char *s)
{
	size_t	i;

	i = 0;
	while (s[i] && (s[i] == 32 || (s[i] >= 9 && s[i] <= 13)))
		i++;
	if (s[i] == 0)
		exit (0);
}